sea_level = float(input("Enter the sea level in meters: "))
if sea_level >= 10000:
    print("Welcome to EXOSPHERE")
elif sea_level >= 690:
    print("Welcome to EXOSPHERE")
elif sea_level >= 85:
    print("Welcome to MESOSPHERE")
elif sea_level >= 50:
    print("Welcome to STRATOSPHERE")
elif sea_level >= 20:
    print("Welcome to TROPOSPHERE")
else:
    print("You are below the sea level, welcome to HYPOSPHERE")


